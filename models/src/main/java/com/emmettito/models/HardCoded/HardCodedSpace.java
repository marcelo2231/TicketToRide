package com.emmettito.models.HardCoded;

public class HardCodedSpace {
    private Float x;
    private Float y;
    private Float angle;

    public HardCodedSpace(Float x, Float y, Float angle){
        this.x = x;
        this.y = y;
        this.angle = angle;
    }

    public Float getX() {
        return x;
    }

    public Float getY() {
        return y;
    }

    public Float getAngle() {
        return angle;
    }

    public void setX(Float x) {
        x = x;
    }

    public void setY(Float y) {
        y = y;
    }

    public void setAngle(Float angle) {
        angle = angle;
    }
}
